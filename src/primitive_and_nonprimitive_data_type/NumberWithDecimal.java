package primitive_and_nonprimitive_data_type;

public class NumberWithDecimal 
{

	public static void main(String[] args) 
	
	{
		
	//In this program we are covering float and double data type.
		
		float height;  //Variable declaration (Allocating/Reserving memory)
		
		height=5.000123400343333f; //Variable Initialization (Assigning or Inserting value)
		
		
		System.out.println("My height is "+height); //Usage (Printing/ or any other operations)
		
		
	   double distance; //Variable declaration (Allocating/Reserving memory)
	   
	   distance=6.0034445345333d; //Variable Initialization (Assigning or Inserting value)
	   
	   System.out.println("The distance is "+distance); //Usage (Printing/ or any other operations)
	   
	   
	   System.out.println("The distance is "+distance+"  ,My height is "+height );
	      

	}

}
